var position = $(window).scrollTop(); 

// should start at 0

$(window).scroll(function(e) {
    var scroll = $(window).scrollTop();
    if(scroll > position) {  /*scroll down */
        $('.m').removeClass("mobile_pull");
        if(!$('.nav-icon').hasClass("menu_open_desktop")){
            $('.moveaway').removeClass("back");
            $('.moveaway').addClass("moveaway1");


            $('.name').removeClass('moveNameLeft');
            $('.name').addClass('moveNameRight');
        }

        

        $('.text1').removeClass('moveText2');
        $('.text1').addClass('moveText');
        
        $('.text2').removeClass('moveText3');
        $('.text2').addClass('moveText1');


        $('body').addClass('slide_down');
        $('body').removeClass('slide_up');
    } else {
        if(!$('.nav-toggle').hasClass('menu_open')){
            if(!$('.nav-icon').hasClass('menu_open_desktop')){
                $('.moveaway').addClass("back");
                $('.moveaway').removeClass('moveaway1');
            
        
                $('.name').removeClass('moveNameRight');
                $('.name').addClass('moveNameLeft');
            }
        }
        $('.text1').removeClass('moveText');
        $('.text1').addClass('moveText2');

        $('.text2').removeClass('moveText1');
        $('.text2').addClass('moveText3');

        $('body').removeClass('slide_down');
        $('body').addClass('slide_up');
    }
    position = scroll;
});

